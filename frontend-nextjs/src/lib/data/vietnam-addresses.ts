// Helper functions
export function getProvinceByCode(code: string): Province | undefined {
  return vietnamAddresses.find(p => p.code === code);
}

export function getDistrictByCode(provinceCode: string, districtCode: string): District | undefined {
  const province = getProvinceByCode(provinceCode);
  return province?.districts.find(d => d.code === districtCode);
}

export function getWardByCode(provinceCode: string, districtCode: string, wardCode: string): Ward | undefined {
  const district = getDistrictByCode(provinceCode, districtCode);
  return district?.wards.find(w => w.code === wardCode);
}
